/******************************************************************************/
void crc16_ccitt_single(uint16_t *crc, const uint8_t chr) {
    uint8_t tmp = (uint8_t)(*crc >> 8U) ^ chr;
    *crc = (*crc << 8U) ^ crc16_ccitt_table[tmp];
}


/******************************************************************************/
uint16_t crc16_ccitt(const uint8_t block[],
                     size_t blockLength,
                     uint16_t crc)
{
    size_t i;

    for (i = 0U; i < blockLength; i++) {
        uint8_t tmp = (uint8_t)(crc >> 8U) ^ block[i];
        crc = (crc << 8U) ^ crc16_ccitt_table[tmp];
    }
    return crc;
}


unsigned short good_crc = 0xFFFF;
#define           poly     0x1021
void augment_message_for_good_crc()
{
    unsigned short i, xor_flag;

    for (i=0; i<16; i++)
    {
        if (good_crc & 0x8000)
        {
            xor_flag= 1;
        }
        else
        {
            xor_flag= 0;
        }
        good_crc = good_crc << 1;

        if (xor_flag)
        {
            good_crc = good_crc ^ poly;
        }
    }
}

void update_good_crc(unsigned short ch)
{
    unsigned short i, v, xor_flag;

    /*
    Align test bit with leftmost bit of the message byte.
    */
    v = 0x80;

    for (i=0; i<8; i++)
    {
        if (good_crc & 0x8000)
        {
            xor_flag= 1;
        }
        else
        {
            xor_flag= 0;
        }
        good_crc = good_crc << 1;

        if (ch & v)
        {
            /*
            Append next bit of message to end of CRC if it is not zero.
            The zero bit placed there by the shift above need not be
            changed if the next bit of the message is zero.
            */
            good_crc= good_crc + 1;
        }

        if (xor_flag)
        {
            good_crc = good_crc ^ poly;
        }

        /*
        Align test bit with next bit of the message byte.
        */
        v = v >> 1;
    }
}


uint16_t crc16_ccitt2(const uint8_t block[],
                     size_t blockLength)
{
    size_t i;
    good_crc = 0xFFFF;
    for (i = 0U; i < blockLength; i++) {
    	update_good_crc(block[i]);
    }
    augment_message_for_good_crc();
    return good_crc;
}

unsigned short crc16_ccitt3(char *ptr, int count)
{
   int  crc;
   char i;
   crc = 0xff;
   while (--count >= 0)
   {
      crc = crc ^ (int) *ptr++ << 8;
      i = 8;
      do
      {
         if (crc & 0x8000)
            crc = crc << 1 ^ 0x1021;
         else
            crc = crc << 1;
      } while(--i);
   }
   return (crc);
}

const int overflow = 0x100, modulus = 0x11D;  // 0x11b for AES GF(2^8) representation -->  x8 + x4 + x3 + x + 1

int gf2n_multiply(int a, int b) {
    int sum = 0;
    while (b > 0) {
        if (b & 1) sum = sum ^ a;             // if last bit of b is 1, add a to the sum
        b = b >> 1;                           // divide b by 2, discarding the last bit
        a = a << 1;                           // multiply a by 2
        if (a & overflow) a = a ^ modulus;    // reduce a modulo the AES polynomial
    }
    return sum;
}


