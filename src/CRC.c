unsigned short usMBCRC16( unsigned char * pucFrame, unsigned short usLen )
{
	volatile unsigned char           ucCRCHi = 0xFF;
	volatile unsigned char           ucCRCLo = 0xFF;
    int             iIndex;

    while( usLen-- )
    {
        iIndex = ucCRCLo ^ *( pucFrame++ );
        ucCRCLo = ( unsigned char )( ucCRCHi ^ aucCRCHi[iIndex] );
        ucCRCHi = aucCRCLo[iIndex];
    }
    return ( unsigned short )( ucCRCHi << 8 | ucCRCLo );
}


// Compute the MODBUS RTU CRC
unsigned short ModRTU_CRC(unsigned char *buf, int len)
{
	unsigned short crc = 0xFFFF;

  for (int pos = 0; pos < len; pos++) {
    crc ^= (unsigned short)buf[pos];          // XOR byte into least sig. byte of crc

    for (int i = 8; i != 0; i--) {    // Loop over each bit
      if ((crc & 0x0001) != 0) {      // If the LSB is set
        crc >>= 1;                    // Shift right and XOR 0xA001
        crc ^= 0xA001;
      }
      else                            // Else LSB is not set
        crc >>= 1;                    // Just shift right
    }
  }
  // Note, this number has low and high bytes swapped, so use it accordingly (or swap bytes)
  return crc;
}

